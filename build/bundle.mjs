/**
 * Bucket build — produce the self-contained Workflow script and the Launcher CLI.
 *
 * Per ADR-0005 the deterministic core is authored as plain, tested modules and
 * inlined into the Workflow at build time (the Workflow sandbox cannot import or
 * read files at runtime). This step:
 *
 *   1. Bundles the pure core (`src/core/inline-entry.ts`) into an IIFE assigned
 *      to `__bucketCore`, and injects it into the Workflow source at the
 *      `__BUCKET_CORE_IIFE__` marker — keeping `export const meta` first.
 *   2. Bundles the `resolve-config` CLI (Node) that the Launcher runs to resolve
 *      and validate config before starting the Workflow. Default prompt files
 *      from `prompts/` are inlined via esbuild's text loader so the CLI can
 *      seed `.bucket/` on first run.
 *
 * Outputs: dist/bucket.workflow.js (Workflow runtime), dist/resolve-config.mjs (Node).
 */

import { build } from "esbuild";
import { readFile, writeFile, mkdir } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const p = (rel) => resolve(root, rel);

const CORE_MARKER = "// __BUCKET_CORE_IIFE__";

async function bundleCoreIIFE() {
  const result = await build({
    entryPoints: [p("src/core/inline-entry.ts")],
    bundle: true,
    write: false,
    format: "iife",
    globalName: "__bucketCore",
    platform: "neutral",
    target: "es2022",
    legalComments: "none",
  });
  return result.outputFiles[0].text.trimEnd();
}

async function buildWorkflow() {
  const [source, coreIIFE] = await Promise.all([
    readFile(p("src/workflow/bucket.workflow.js"), "utf8"),
    bundleCoreIIFE(),
  ]);

  if (!source.includes(CORE_MARKER)) {
    throw new Error(`bundle: workflow source is missing the "${CORE_MARKER}" injection marker`);
  }

  const banner =
    "// GENERATED by build/bundle.mjs — do not edit.\n" +
    "// Source: src/workflow/bucket.workflow.js + inlined src/core (ADR-0005).\n";
  const injected = source.replace(
    CORE_MARKER,
    `// --- inlined deterministic core (ADR-0005) ---\n${coreIIFE}\n// --- end inlined core ---`,
  );

  await writeFile(p("dist/bucket.workflow.js"), banner + injected, "utf8");
}

async function buildResolveCli() {
  await build({
    entryPoints: [p("src/cli/resolve-config.ts")],
    bundle: true,
    outfile: p("dist/resolve-config.mjs"),
    format: "esm",
    platform: "node",
    target: "node18",
    banner: { js: "#!/usr/bin/env node" },
    legalComments: "none",
    loader: { ".md": "text" },
  });
}

await mkdir(p("dist"), { recursive: true });
await Promise.all([buildWorkflow(), buildResolveCli()]);
console.log("bucket: built dist/bucket.workflow.js and dist/resolve-config.mjs");
